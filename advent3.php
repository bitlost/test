class Trees {

  var $map;
  var $cur_x = 0;
  var $cur_y = 0;
  var $lines;
  
  function __construct($map) {
    $this->map = explode("\n", $map);
    $this->lines = count($this->map);
  }
  
  function sled($right, $down) {
    $tree_count = 0;
    
    while ($this->cur_y < $this->lines) {      
      if ($this->isTree($this->cur_x, $this->cur_y)) $trees++;
      $this->cur_x += $right;
      $this->cur_y += $down;
    }
    
    //reset map
    $this->cur_x = 0;
    $this->cur_y = 0;
    
    return $tree_count;
    
  }
  
  private function getCoordinate($x, $y) {
    $length = strlen($this->map[$y]);
    
    if ($x > $length) {
      $x = ($x - (floor(($x)  / $length) * $length));
    }
    
    return substr($this->map[$y], $x, 1);
  }

  private function isTree($x, $y) {
    return ($this->getCoordinate($x,$y) == "#");
  }
}
